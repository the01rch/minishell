#include "../inc/libft.h"

void	*ft_memcpy(void *dest, const void *src, size_t n)
{
	unsigned char	*d;
	unsigned char	*s;
	size_t			i;

	d = (unsigned char *)dest;
	s = (unsigned char *)src;
	i = 0;
	while (d && i < n)
	{
		if (d != NULL || s != NULL)
			d[i] = s[i];
		i++;
	}
	return ((void *)d);
}
